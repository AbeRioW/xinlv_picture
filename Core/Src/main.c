// 简单的心率处理算法
void ProcessHeartRateData(void) {
    static uint32_t lastPeakTime = 0;
    static uint32_t peakInterval = 0;
    static uint8_t peakCount = 0;
    
    // 寻找峰值
    uint8_t i;
    for(i = 1; i < 127; i++) {
        if(waveformData[i] > waveformData[i-1] && 
           waveformData[i] > waveformData[i+1] &&
           waveformData[i] > 40) { // 阈值过滤
            uint32_t currentTime = HAL_GetTick();
            if(lastPeakTime > 0) {
                peakInterval += (currentTime - lastPeakTime);
                peakCount++;
                
                if(peakCount >= 3) { // 计算平均心率
                    heartRate = 60000 / (peakInterval / peakCount);
                    heartRateValid = 1;
                    peakInterval = 0;
                    peakCount = 0;
                }
            }
            lastPeakTime = currentTime;
            break;
        }
    }
    
    // 超时重置
    if(HAL_GetTick() - lastPeakTime > 3000) {
        lastPeakTime = 0;
        peakInterval = 0;
        peakCount = 0;
        heartRateValid = 0;
    }
}
